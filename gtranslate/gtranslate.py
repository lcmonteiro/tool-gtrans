from .providers import GoogleAPI
from .providers import GoogleBrowser
# -------------------------------------------------------------------------------------------------
# Translator 
# -------------------------------------------------------------------------------------------------
class Translator:
    # ----------------------------------------------------------------------------------
    # initialization
    # ----------------------------------------------------------------------------------
    def __init__(self, to_lang='en', from_lang=None):
        self.__providers = [
            GoogleAPI    (to_lang, from_lang),
            GoogleBrowser(to_lang, from_lang)]
    # ----------------------------------------------------------------------------------
    # translate
    # ----------------------------------------------------------------------------------
    def translate(self, data):
        for provider in self.__providers:
            try:
                return provider.translate(data)
            except:
                pass
        raise RuntimeError('translation failed')
